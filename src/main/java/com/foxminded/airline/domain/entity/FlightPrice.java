package com.foxminded.airline.domain.entity;

public class FlightPrice {
    private String level;
    private int price;

    public String getLevel() {
        return level;
    }

    public void setLevel(String level) {
        this.level = level;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }
}
